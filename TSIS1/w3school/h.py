#we can assign values to many variables in one line
#number of values should match the number of variables
x, y, z = "orange", "banana", "cherry"
print(x)
print(y)
print(z)

#we can assign one value to many variables at the time
a = b = c = "apple"
print(a)
print(b)
print(c)

#we can unpack a collection using varibles

fruits = ["tangerine", "raspberry", "pineapple"]
h, i, j = fruits
print(h)
print(i)
print(j)

