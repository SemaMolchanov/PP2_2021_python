b = "Hello, World!"
#we can slice string

#from beginning index to the end index(end index not included)
print(b[2:5])

#fro the start to the end index(not included)
print(b[:5])

#from the beginning index to the end
print(b[2:])

#from beginning index, counted from the end
#to the end index(not included), counted from the end
print(b[-5:-2])